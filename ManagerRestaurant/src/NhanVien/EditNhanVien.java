    private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
        if(this.txtManv.getText().length()==0)

        JOptionPane.showMessageDialog(null, "Mã Nhân Viên  không được bỏ trống", "Thông Báo",1);

        else
        if(this.txtManv.getText().length()>10)
        JOptionPane.showMessageDialog(null, "Mã Nhân Viên  không được quá 10 ký tự ","Thông Báo",1);
        else

        if(this.txtTenNV.getText().length()==0)
        JOptionPane.showMessageDialog(null," Tên  Nhân Viên  không được bỏ trống","Thông Báo",1);
        else
        if(this.txtLuong.getText().length()==0)
        JOptionPane.showConfirmDialog(null, "Lương  Nhân Viên  không được bỏ trống","Thông Báo",1);
        else
        {
            AddNhan.UpdateNV(mnv,this.txtManv.getText().trim(), this.txtTenNV.getText(),(JTextField)this.txtNgaysinh.getDateEditor().getUiComponent(), this.cbGT.getSelectedItem().toString(), this.txtLuong.getText().toString(), this.cbCV.getSelectedItem().toString(), this.txtDiachi.getText(),this.txtSDT.getText(),this.txtghichu.getText());
            showCT();
            ProcessCtr(true);
        }
    }//GEN-LAST:event_btEditActionPerformed

