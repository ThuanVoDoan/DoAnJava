    private void tbNVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbNVMouseClicked
          ProcessCtr(true);   
        
        try {
            int row = this.tbNV.getSelectedRow();
            String IDrow =(this.tbNV.getModel().getValueAt(row, 1)).toString();
            String sql1="select distinct * from NhanVien where TenNV=N'"+IDrow+"'";
            ResultSet rs = UpdataTable.ShowTextField(sql1);
            if(rs.next())
            {
               mnv  =rs.getString("TenNV");

                this.txtManv.setText(rs.getString("MaNV"));
                this.txtTenNV.setText(rs.getString("TenNV"));
                Date NS=rs.getDate("NgaySinh");
                this.txtNgaysinh.setDate(NS);
                this.cbGT.setSelectedItem(rs.getString("GioiTinh"));
               this.txtLuong.setText(rs.getString("Luong"));
                this.cbCV.setSelectedItem(rs.getString("ChucVu"));
                this.txtDiachi.setText(rs.getString("Diachi"));
                this.txtSDT.setText(rs.getString("SDT"));
                this.txtghichu.setText(rs.getString("GhiChu"));
               
                
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tbNVMouseClicked

